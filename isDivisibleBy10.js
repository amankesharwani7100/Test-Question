// 3. Write an Arrow Function to Return a Boolean if a number is divisible by 10?

// Arrow function to check if a number is divisible by 10
const isDivisibleBy10 = (number) => number % 10 === 0;

// Example usage:
console.log(isDivisibleBy10(20)); // Output: true
console.log(isDivisibleBy10(15)); // Output: false

